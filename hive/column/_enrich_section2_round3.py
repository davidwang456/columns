def chapter_num(path: str) -> int | None:
    m = re.search(r"第 (\d+) 章", os.path.basename(path))
    return int(m.group(1)) if m else None


def patch(path: str) -> bool:
    with open(path, "r", encoding="utf-8") as f:
        text = f.read()
    if MARKER in text:
        return False
    a = text.find("## 2 项目设计")
    if a < 0:
        return False
    b = text.find("\n---\n\n## 3 项目实战", a)
    if b < 0:
        return False
    n = chapter_num(path)
    if n is None or n not in ROUND3:
        return False
    block = ROUND3[n].format(marker=MARKER).strip()
    new_text = text[:b] + "\n\n" + block + "\n\n" + text[b:]
    with open(path, "w", encoding="utf-8", newline="\n") as f:
        f.write(new_text)
    return True


def main():
    files = sorted(glob.glob(os.path.join(HERE, "第*.md")))
    for p in files:
        if patch(p):
            print("patched", os.path.basename(p))


if __name__ == "__main__":
    main()
